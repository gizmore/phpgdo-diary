<h1>Trinkregeln 2021</h1>

<p>
Ich bin ein Alkoholiker und versuche gerade nur sehr wenig zu trinken.
Dies sind meine Regeln, so dass ich mit dem Alkohol leben kann und nicht trocken sein muss.
</p>

<ul>
  <li>1. Niemals mehr alleine trinken.</li>
  <li>2. Keinen Vodka trinken.
         Besonders nicht mit Lazer.</li>
  <li>3. Drei Mahlzeiten pro Tag.</li>
  <li>4. Tischtennis wieder im Verein spielen. Sport und Gymnastik.</li>
  <li>5. Mehr Leute treffen.</li>
  <li>6. Selbsthilfegruppe aufsuchen.</li>
  <li>7. Es ist Freitag! Ich kann heute oder morgen ein paar Bier trinken.</li>
</ul>

<p>
Einige Regeln wurden bereits gebrochen; aber nicht allzu sehr.
</p>
