<h2>Und Tschüss!</h2>

<p>Es war in der Entgiftung eine Hausaufgabe, einen Abschiedsbrief an den Alkohol zu schreiben.
Weil ich den Alkohol sehr liebte, habe ich ihn in Gedichtform verfasst.</p>

<pre>
Und Tschüss!
============

Oh Alkohol, Oh Alkohol, ...
Du machst mich dumm, Du machst mich hohl.
Drum lass ich Dich allein zurück,
tät ich es nicht würd ich verrückt.

Ich mochte Dich einmal so sehr.
Ja aber jetzt will ich nicht mehr.
Die Leber hart, der Magen wund,
und das ist nicht der einz'ge Grund.

Doch fangen wir von Vorne an.
Ich schreibe Dir wie es begann.

--------------------------------------------

Der Erstkontakt durch Opa Jürgen
war lustig nett, musst' niemals würgen.
Gekostet hatte ich Dich nicht.
Doch es geht weiter im Gedicht.

Mit 14, Basti, Markus, ich...
In einem Wald, versuchten Dich.
Wir tranken Bier, wir tranken viel.
Ein heißer Sommer, Kartenspiel.

Wir waren lustig, mutig, high...
Leider blieb es nicht dabei.
Wir wurden kirchlich konfirmiert.
Auch Biederkinder haben Dich probiert.

Der Biederkinders erster Kicks,
für uns deren Konsum ein Witz.
Auf Klassenfahrt sah der Lehrer rot.
Es gab für Dich schnell ein Verbot.

Die Clique hängt täglich bei uns rum.
Fast täglich Bier, das macht nicht dumm.
So wurdest Du zu mei'm Begleiter
Das Leben läuft, wie ging es weiter?

Zu Feiern gab es auch mal Mischen,
ich mochte Dich nie wieder missen.
Bald hatten wir auch einen Dreier,
kifft man mit Dir wird man noch higher.

Weisst Du noch, wie war das krass;
Mit 3 Promill' Junggesellschaft.
Unsere Liebe akzeptiert,
was ist denn bloß mit uns passiert?

Mit 20 Zivi, Altenheim.
Ein Opa gab mir Bier allein.
Ich trank es abends schon im Flur,
als Tageswerk Belohnung nur.

Gelebt zu viert in 'ner WG.
Zusamm' tuts finanziell nicht weh.
Paletten Bier und hoch die Gläser.
Für uns bezahlt hat meist der Lazer.

Lazer ist mein bester Freund,
Wir zogen zu dritt aus; Vereint.
Doch bald hats't Du die Oberhand...
Alk und ich, das gut ich fand.

Ich war so oft mit Dir allein.
Ich lernte Hacken, Bier muss sein.
Darauf ein Bong, spür Dich wie nie.
In's Krankenhaus, Schizophrenie.

Kaum kam ich raus, warst für mich da.
Ich fühlte mich Dir ja so nah.
Das Kiffen erstmal abgelegt.
Die Zeit mit Dir so schön erlebt.

So gingen dann die Jahre hin.
Ich suchte wohl auch nach dem Sinn.
Wir waren ein Jahrzehnt allein,
tatest mir gut, so war der Schein.

-------------------------------------------------------------

Doch eines Tag's geschah es dann:
Ich fing mit Dir als Vodka an.
Zwei kurze Schnaps an allen Tagen.
Du tat'st mir gut, was soll ich sagen?

Wir flogen hoch für 7 Jahre,
ich dachte echt es ist das Wahre.
Bald jedoch gab es Probleme,
Du warst zu hart, ich zahl Tantieme.

Man sagt Liebe geht durch den Magen,
Ich bin verrückt nach Dir, trotz Schaden.
Denk' nur an Dich, kann nicht mehr essen.
Tu' alles andere vergessen.

Ne Abmahnung, mir doch egal.
Der Freundeskreis steht vor der Wahl.
Entzug ein Stück, zurück zu Dir,..
Doch kontrolliert, vielleicht ein Bier.

Es reisst mich täglich hin und her.
Ich denke mir, ich will Dich mehr.
Die Tage werden immer kürzer...
Danke mein Schnaps, in's Koma Stürzer.

Räum' nicht mehr auf,
will nicht mehr essen.
Doch wer will Dich schon vergessen.

Irgendwie bemerk' ich dann,
dass ohne Dich ich nicht mehr kann.
Doch du allein führst schnell in' Tot.
Auch alle and'ren sehen rot.

Drum sag ich Dir nun nur einmal.
Ich stehe wieder vor der Wahl.

Goodbye und Tschüss mein lieber Schatz.
Wir trennen uns mit diesem Satz.

